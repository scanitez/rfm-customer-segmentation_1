import streamlit as st
import pandas as pd
import joblib

model = joblib.load("rfm_classifier.pkl")
scaler = joblib.load("rfm_scaler.pkl")

st.set_page_config(page_title="Customer Segment Predictor", page_icon="ğŸ›ï¸")
st.title("ğŸ›ï¸ Customer Segment Predictor")
st.markdown("Enter customer RFM values to predict the segment.")

recency = st.number_input("Recency (days since last purchase)", min_value=0, value=30)
frequency = st.number_input("Frequency (number of purchases)", min_value=0, value=5)
monetary = st.number_input("Monetary (total spend)", min_value=0, value=500)

if st.button("Predict Segment"):
    input_data = pd.DataFrame({
        'Recency': [recency],
        'Frequency': [frequency],
        'Monetary': [monetary]
    })
    scaled_data = scaler.transform(input_data)
    prediction = model.predict(scaled_data)[0]

    st.success(f"ğŸ“Š Predicted Segment: Cluster {prediction}")

    segment_info = {
        0: "ğŸ’ High-value loyal customer. Keep engaged!",
        1: "ğŸ›ï¸ Average customer. Could be nurtured.",
        2: "ğŸ“‰ At-risk customer. Consider reactivation.",
        3: "ğŸš¨ Low-value and inactive customer.",
        4: "ğŸ”„ Recent buyer. Watch for loyalty."
    }

    st.info(segment_info.get(prediction, "Segment interpretation unavailable."))